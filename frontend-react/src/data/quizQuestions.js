// Pick 10 questions seeded by today's date — same set for all users on the same day
export function getDailyQuestions(lang = 'en') {
  const today = new Date()
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate()
  const shuffled = [...ALL_QUESTIONS].sort((a, b) => {
    const ha = ((seed ^ (a.id * 2654435761)) >>> 0)
    const hb = ((seed ^ (b.id * 2654435761)) >>> 0)
    return ha - hb
  })
  const picked = shuffled.slice(0, 10)

  if (lang === 'kn') {
    return picked.map(q => {
      const kn = ALL_QUESTIONS_KN.find(k => k.id === q.id)
      if (!kn) return q
      return { ...q, question: kn.question, options: kn.options, explanation: kn.explanation }
    })
  }
  return picked
}
